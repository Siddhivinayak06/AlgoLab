import PDFDocument from "pdfkit"

interface ExperimentReportInput {
  id: string
  algorithm: string
  mode: string
  arraySize: number
  executionTime: number
  comparisons: number
  operations: number
  dataType: string
  createdAt: Date
}

export function buildExperimentPdf(experiment: ExperimentReportInput): Promise<Buffer> {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ margin: 50 })
    const chunks: Buffer[] = []

    doc.on("data", (chunk: Buffer) => chunks.push(Buffer.from(chunk)))
    doc.on("end", () => resolve(Buffer.concat(chunks)))
    doc.on("error", reject)

    doc.fontSize(22).text("Algorithm Performance Report", { align: "left" })
    doc.moveDown(0.5)
    doc.fontSize(10).fillColor("#666").text(`Generated: ${new Date().toISOString()}`)
    doc.fillColor("#000")
    doc.moveDown(1)

    doc.fontSize(14).text("Experiment Overview")
    doc.moveDown(0.5)
    doc.fontSize(11)
    doc.text(`Experiment ID: ${experiment.id}`)
    doc.text(`Algorithm: ${experiment.algorithm}`)
    doc.text(`Mode: ${experiment.mode}`)
    doc.text(`Data Type: ${experiment.dataType}`)
    doc.text(`Executed At: ${experiment.createdAt.toISOString()}`)

    doc.moveDown(1)
    doc.fontSize(14).text("Performance Metrics")
    doc.moveDown(0.5)
    doc.fontSize(11)
    doc.text(`Array Size: ${experiment.arraySize}`)
    doc.text(`Execution Time: ${experiment.executionTime.toFixed(3)} ms`)
    doc.text(`Comparisons: ${experiment.comparisons}`)
    doc.text(`Operations: ${experiment.operations}`)

    const operationsPerComparison =
      experiment.comparisons > 0 ? experiment.operations / experiment.comparisons : 0
    const operationsPerElement = experiment.arraySize > 0 ? experiment.operations / experiment.arraySize : 0

    doc.moveDown(1)
    doc.fontSize(14).text("Derived Indicators")
    doc.moveDown(0.5)
    doc.fontSize(11)
    doc.text(`Operations per Comparison: ${operationsPerComparison.toFixed(3)}`)
    doc.text(`Operations per Element: ${operationsPerElement.toFixed(3)}`)

    doc.moveDown(1)
    doc.fontSize(10).fillColor("#555")
    doc.text(
      "This report was generated by Algorithm Performance Analyzer backend using PDFKit.",
      { align: "left" }
    )

    doc.end()
  })
}
